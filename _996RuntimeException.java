package net.noyark._996;

/**
 * 当你不想让你的996怨念展露头角,可以用它,很难被发现
 * @author magiclu550
 */
public class _996RuntimeException extends RuntimeException {

    public _996RuntimeException() {
    }

    public _996RuntimeException(String message) {
        super(message);
    }

    public _996RuntimeException(String message, Throwable cause) {
        super(message, cause);
    }

    public _996RuntimeException(Throwable cause) {
        super(cause);
    }

    public _996RuntimeException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
